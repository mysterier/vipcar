$("#orderCommit").click(function(){
	var validate = true;
	var contacter_name = $("input[name=contacter_name]");
	var contacter_phone = $("input[name=contacter_phone]");
	var pickup_time = $("input[name=pickup_time]");
	var pickup_place = $("input[name=pickup_place]");
	
	var error_msg = [
		'联系人不能为空！',
		'联系电话不能为空！',
		'上车时间不能为空！',
		'上车地点不能为空！',
		'下车地点不能为空！',		
	];
	var params = [contacter_name.val(),contacter_phone.val(),pickup_time.val(),pickup_place.val(),$(".drop_place").val()];
	var msg = '';
	for (i=0;i<params.length;++i) {
		if (!params[i]) {
			msg += error_msg[i];			
			validate = false;
		}
	}

	if (validate) {
		$("form").submit();
	} else {
		alertError(msg);
	}
});


<p class="text-muted">在下述情况下，任何一方均可在书面通知另一方后立即终止本协议：</p>
<p class="text-muted">(a) 另一方停业、清算或解散；</p>
<p class="text-muted">(b) 另一方无法支付到期债务，或其全部或部分财产被指定给破产接管人、行政接管人或管理人（或依照其公司所在地或公司设立地的法律规定的任何类似官员或程序）接管，或正面临任何破产程序；</p>
<p class="text-muted">(c) 另一方违反本协议的规定，并且该违约无法补救。</p>


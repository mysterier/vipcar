<strong class="brown-title">4.关于费用</strong>
</p>
<p class="text-muted">
      Q：账户充值有额度要求吗？
      <br/>
      A：充值后只要您的账号余额高于订单预估费用即可。
      <br/>
      Q：可以验证多张信用卡吗？
      <br/>
      A：同一名下信用卡只能绑定一个众择用车帐号。
</p>
<p>
<strong class="brown-title">5.关于用车</strong>
</p>
<p class="text-muted">
      Q：预订时如何使用优惠券？
       <br/>
      A：预订车辆时请在订单提交前选择需要使用的优惠券，在服务结束后系统会自动减免优惠券金额。
      <br/>
      Q：变更订单需要重新下单吗？
      <br/>
      A：如果变更乘车人信息则无需重新下单，致电400-965-2886客服热线为您人工处理变更。其它信息（变更使用日期、时间、起始地、目的地等）需取消原订单，重新下单。<br/>
      Q：接机服务，司机会在哪里等待乘车人？
      <br/>
      A：司机在出站口等您。若需更改等候地址，可以根据您的具体情况致电司机沟通详细的上车地点。
</p>
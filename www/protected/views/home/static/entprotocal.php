<p class="text-muted">6.1 未经另一方事先书面许可，一方在任何情况下均不得将另一方的机密信息泄漏给第三方。</p>
<p class="text-muted">6.2 尽管前款规定，乙方可向其职员、顾问及供应商透露机密信息，前提是该种透露应为乙方履行本协议之义务所必需，且上述职员、顾问及供应商同意承担与本协议中所规定的保密义务相同或更严格的保密义务。</p>
<p class="text-muted">6.3 为本协议之目的，“机密信息”是指与一方有关、被其指定为机密的任何信息，或是另一方应合理认定为机密的任何信息。机密信息包括但不限于甲方名单以及与一方业务有关的、另一方以任何方式占有或了解的各种信息及材料，但不包括下列内容：</p>
<p class="text-muted">(a) 接受方在从另一方接受之前就已经拥有的信息和材料；</p>
<p class="text-muted">(b) 一方从第三方合法获得的无需承担保密义务的信息和材料；</p>
<p class="text-muted">(c) 非因接受方违反保密义务，当前或之后为公众所知的信息和材料；</p>
<p class="text-muted">(d) 未接触另一方机密信息的接受方开发人员独自开发的信息和材料；</p>
<p class="text-muted">(e) 经接受信息一方书面授权公开或书面指定为不再保密或不再专有的任何信息或材料。</p>


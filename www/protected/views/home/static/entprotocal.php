<p class="text-muted">乙方声明并保证：</p>
<p class="text-muted">(a)其为依照公司设立地的法律规定设立并合法存续的公司；</p>
<p class="text-muted">(b) 其已采取所有必要措施取得本协议的签署、交付及履行的授权；</p>
<p class="text-muted">(c) 为甲方提供服务的车辆都来自有合法资质的车辆；</p>
<p class="text-muted">(d) 为甲方提供车辆驾驶服务的人员都来自有合法资质的劳务服务公司；</p>
<p class="text-muted">(e) 其有权签订并履行本协议项下的义务，并且无需任何其他人的同意；</p>
<p class="text-muted">(f) 服务条款或甲方对服务的使用不会侵犯任何第三方的知识产权；</p>


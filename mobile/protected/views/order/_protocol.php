$("#showticket").on('change',function(){
	var coupon_cost = $("#showticket option:selected").attr("coupon_cost");
	var income = getIncome()-coupon_cost;
	if (coupon_cost) {
		income = (income > 0) ? income : 0;
		$(".rmb").text(income);
		$("#estimated_cost").val(income);
	} else {
		income = getIncome();
		$(".rmb").text(income);
		$("#estimated_cost").val(income);
	}	
});


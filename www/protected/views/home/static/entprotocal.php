	<h3 class="text-primary text-center">《众择用车》企业服务协议</h3>
<p>迎使用《众择用车》企业服务，您在注册成功并提交用车订单后，即成为《众择用车》企业级会员用户，享受企业用户之权利和服务。
上海苏先信息技术有限公司向您提供的是信息服务，《众择用车》是上海苏先信息技术有限公司旗下用车信息平台，您在访问《众择用车》信息平台后，即可通过该信息平台获知第三方汽车租赁服务提供商和第三方驾驶员服务提供商的相关信息。
	以下协议条款中，甲方为您的企业（简称甲方），乙方为《众择用车》（简称乙方）</p>
	<p class="text-primary">1. 服务形式及服务规则</p>
<p class="text-muted">1.1 服务形式</p>
<p class="text-muted">甲方可通过《众择用车》企业端预约专车服务。甲方乘车人员在行程结束后无需支付用车费及增值服务费，相应金额将采用月结方式进行结算。乙方根据甲方需求，可提供专属VIP客服、企业结算统一对帐及实时管理用车信息等增值服务。乙方保留对各项功能升级及新增功能调整收费标准的权利。</p>
<p class="text-muted">1.2 服务期限:自本协议生效之日起一年。</p>
<p class="text-muted">1.3 价格、支付结算方式及开票</p>
<p class="text-muted">（1）签约3个工作日内，乙方为甲方开通企业会员账号及权限。</p>
<p class="text-muted">（2）乙方专车服务计价方式以乙方实时公布的价格表为准，甲方可随时向乙方索取该价格表的最新版本。（具体价格见附件）</dd>
<p class="text-muted">（3）乙方将在每月5号和甲方结算上一个月发生的总体费用，在每月10号前甲方将款项打入乙方指定的账户。如双方对某一笔款项有异议时，此笔费用可先搁置延后支付，其他费用需照常支付。</dd>
<p class="text-muted">（4）上述费用的开票方式为：乙方收到甲方款项后5个工作日内，为甲方开具全额发票。</p>
<p class="text-muted">（5）如甲方未能按时结算乙方服务费用，乙方有权中止支持甲方使用企业帐号发出的叫车服务。</p>
<p class="text-muted">（6）甲方可随时向其企业帐号充值车费，乙方依据甲方实际使用费用向甲方开具相应发票。</p>

<p class="text-primary">2. 责任限制</p>
<p class="text-muted">在法律允许的情况下，因本协议而产生的或与本协议有关的乙方的责任不应超过甲方依照本协议支付的合同价款总额。</p>

<p class="text-primary">3. 数据所有权</p>
<p class="text-muted">乙方承认甲方数据现在且将来均属于甲方财产。</p>

